#Arithmetic Operators
num1 = 34
num2 = 10



print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2) #division
print(num1 % num2) #modulus takes the two values and divide them and take return the remainder


#comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #equal to
print(num1 != num2) #not equal to

#Assignement operators - used to assign values to valuables
number = 12

print(number)

number += 2  #addition  and assignment

print(number)

b = 56
b -= 1

print(b)

#logic operators - and, or, not - more than one condition
print( 34 < 10 and 89 > 5)  #only return a true if all the conditions are true
print( 34 < 10 or 89 > 5)   #only return a true if any of the conditions is true
print(not( 34 < 10 or 89 > 5)) # reverses the results of the previous




